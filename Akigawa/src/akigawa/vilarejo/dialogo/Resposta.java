package akigawa.vilarejo.dialogo;

public class Resposta {
private String fala;
private int retorno;

	public Resposta(String fala, int retorno){
		this.fala = fala;
		this.retorno = retorno;
	}

	public String getFala() {
		return fala;
	}

	public int getRetorno() {
		return retorno;
	}

	
}
